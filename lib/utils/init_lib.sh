cmd_help() { 
  local cmd="$1"
  local desc="$2"
  printf "Use '%b%s%b' %s \n" "${_CYN}" "$cmd" "${_RST}" "$desc"
}

# ---------- Global defaults ----------
K8S_NS="${K8S_NS:-default}"
NON_INTERACTIVE="${NON_INTERACTIVE:-false}"

