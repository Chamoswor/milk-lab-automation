purge_mysql_data() {
  local data_dir="$SCRIPT_DIR/mysql/data"

  [[ -d "$data_dir" ]] || { info "No MySQL data directory to delete."; return 0; }

  if ${NON_INTERACTIVE}; then
    warn "Skipping deletion of $data_dir in non-interactive mode."
    return 0
  fi

  read -rp "Delete existing MySQL data (users/passwords)? [y/N]: " ans
  [[ $ans =~ ^[Yy]$ ]] || { info "Existing data kept."; return 0; }

  info "Stopping containers and removing volumes …"
  docker compose down -v
  info "Deleting $data_dir …"
  rm -rf "$data_dir"
  info "Data directory removed. MySQL will initialise fresh on next start-up."
}

prompt_secret() {
  local prompt="$1" file="$2" val=""

  if [[ ${use_same,,} == "y" ]]; then
    while true; do
      read -rsp "Enter password for all secrets: " val; printf "\n"
      if [[ -n "$val" ]]; then
        break
      else
        warn "Value cannot be empty. Try again."
      fi
    done
    printf '%s' "$val" > "$file" && chmod 600 "$file"
    info "Saved to $file"
    return 0
  fi

  while true; do
    ${NON_INTERACTIVE} && { err "Non-interactive mode active, but secret required ($prompt)."; return 1; }
    read -rsp "${prompt}: " val; printf "\n"
    if [[ -n "$val" ]]; then
      break
    else
      warn "Value cannot be empty. Try again."
    fi
  done
  printf '%s' "$val" > "$file" && chmod 600 "$file"
  info "Saved to $file"
}

validate_subnet() {
  local subnet="$1"
  if [[ ! "$subnet" =~ ^[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}$ ]]; then
    err "Invalid subnet format: $subnet"
    return 1
  fi
}

create_or_edit_env_file() {
  echo
  info "Generating/updating .env …"
  local env_file="$SCRIPT_DIR/.env"; local mysql_user url_host subnet_ip

  if [[ -f "$env_file" && ${NON_INTERACTIVE} == false ]]; then
    read -rp ".env exists. Overwrite? [y/N]: " ans
    [[ $ans =~ ^[Yy]$ ]] || { warn "Cancelled by user."; return 0; }
  fi

  if ${NON_INTERACTIVE}; then
    err "--non-interactive mode requires pre-defined variables."
    return 1
  fi

  local default_mysql_user="user"
  local default_url_host="localhost"
  local default_subnet_ip="172.20.0.0"

  if [[ -z "$SERVER_NAME" || -z "$NORMAL_MYSQL_USER" || -z "$SUBNET" ]]; then
    warn "Custom environment variables are not set in init.conf, interactive mode will be used."
    echo
    important "Creating .env file for environment variables."
    info "Leave the fields empty to use defaults."
    read -rp "MySQL username (default: $default_mysql_user): " mysql_user
    read -rp "Domain/URL without https:// (default: $default_url_host): " url_host
    read -rp "Subnet Ip (default: $default_subnet_ip): " subnet_ip

    mysql_user="${mysql_user:-$default_mysql_user}"
    url_host="${url_host:-$default_url_host}"
    subnet_ip="${subnet_ip:-$default_subnet_ip}"

  else
    mysql_user="$NORMAL_MYSQL_USER"
    url_host="$SERVER_NAME"
    subnet_ip="$SUBNET"
    echo
    info "Using environment variables from init.conf."
    echo "Using MySQL user: $mysql_user"
    echo "Using domain/URL: $url_host"
    echo "Using subnet IP: $subnet_ip"
  fi

  validate_subnet "$subnet_ip" || {
    err "Invalid subnet IP: $subnet_ip"
    return 1
  }

  FRONTEND_IP="${subnet_ip%.*}.20"
  BACKEND_IP="${subnet_ip%.*}.30"
  NGINX_IP="${subnet_ip%.*}.40"
  MYSQL_IP="${subnet_ip%.*}.50"
  PYTHON_IP="${subnet_ip%.*}.60"

  cat > "$env_file" <<EOF
# Autogenerated $(date +%F)
NODE_ENV=development
VITE_API_URL=https://${url_host}/api
BASE_URL=/
VITE_WS_URL=ws://${url_host}

DB_HOST=mysql
DB_USER=${mysql_user}
DB_NAME=mydb
CORS_ORIGIN=https://${url_host}

# Self-signed cert details
NGINX_SERVER_NAME=${url_host}
NGINX_SERVER_NAME_WWW=www.${url_host}
SSL_DIR=/etc/nginx/ssl
SSL_COUNTRY_NAME=NO
SSL_STATE_OR_PROVINCE_NAME=Trondelag
SSL_LOCALITY_NAME=Trondheim
SSL_ORGANIZATION_NAME=Private

MYSQL_USER=${mysql_user}
MYSQL_DATABASE=mydb

SUBNET=${subnet_ip}/16
FRONTEND_IP=${FRONTEND_IP}
BACKEND_IP=${BACKEND_IP}
NGINX_IP=${NGINX_IP}
MYSQL_IP=${MYSQL_IP}
PYTHON_IP=${PYTHON_IP}
EOF
  chmod 600 "$env_file"
  info "Wrote $env_file"
}

create_or_edit_secrets() {
  info "Creating/updating secrets …"
  local secrets_dir="$SECRETS_DIR" ans
  if [[ -d "$secrets_dir" && ${NON_INTERACTIVE} == false ]]; then
    read -rp "Secrets directory exists. Overwrite? [y/N]: " ans
    [[ $ans =~ ^[Yy]$ ]] || { warn "Cancelled by user."; return 0; }
  fi
  mkdir -p "$secrets_dir" && chmod 700 "$secrets_dir"

  declare -A prompts=(
    [admin-password]="Password for backend user \"admin\""
    [db-password]="Password for MySQL user"
    [mysql-root-password]="Password for MySQL root"
    [session-secret]="Session secret (cookie signing)"
  )
  echo
  important "MySQL root, MySQL user, admin (web-login), og session secrets will be created."
  read -rp "Use same password for all secrets? [y/N]: " use_same
  use_same="${use_same,,}"

  if [[ "$use_same" == "y" ]]; then
    local val file key
    while true; do
      read -rsp "Enter password for all secrets: " val; printf "\n"
      if [[ -n "$val" ]]; then
        break
      else
        warn "Value cannot be empty. Try again."
      fi
    done
    for key in "${!prompts[@]}"; do
      file="$secrets_dir/$key"
      printf '%s' "$val" > "$file" && chmod 600 "$file"
      info "Saved to $file"
    done
    return 0
  fi

  for key in "${!prompts[@]}"; do
    prompt_secret "${prompts[$key]}" "$secrets_dir/$key" || return 1
  done

  info "All secrets saved."
}

